# SPPF CSPC 模块
# -----------------------
class SPPFCSPC(nn.Module):
    def __init__(self, c1, c2=None, *args, **kwargs):
        """
        c1: 输入通道
        c2: 输出通道（可忽略）
        args: parse_model 传入的 YAML 额外参数
        kwargs: 其他关键字参数
        """
        super().__init__()
        # 默认 kernel size
        k = 5
        if len(args) > 0:
            k = args[0]

        c = c1
        c_ = c // 2

        self.cv1 = Conv(c, c_, 1, 1)
        self.cv2 = Conv(c_, c_, 3, 1)
        self.cv3 = Conv(c_, c_, 1, 1)
        self.m = nn.MaxPool2d(kernel_size=k, stride=1, padding=k//2)
        self.cv4 = Conv(c_ * 4, c_, 1, 1)
        self.cv5 = Conv(c_, c, 3, 1)

    def forward(self, x):
        x = self.cv1(x)
        y1 = self.cv2(x)
        y2 = self.m(y1)
        y3 = self.m(y2)
        y4 = self.m(y3)
        out = torch.cat([y1, y2, y3, y4], dim=1)
        out = self.cv4(out)
        return self.cv5(out)

