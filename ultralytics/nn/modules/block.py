# BiFPN
# -----------------------
class BiFPN(nn.Module):
    def __init__(self, c1, c2=None, *args, **kwargs):
        """
        c1: 输入通道
        c2: 输出通道（可忽略）
        *args, **kwargs: 兼容 parse_model
        """
        super().__init__()
        channels = c1  # 默认使用输入通道
        self.w1 = WeightedFusion(2)
        self.w2 = WeightedFusion(2)
        self.w3 = WeightedFusion(2)
        self.w4 = WeightedFusion(2)
        self.p3_conv = Conv(channels, channels, 3, 1)
        self.p4_conv = Conv(channels, channels, 3, 1)
        self.p5_conv = Conv(channels, channels, 3, 1)

    def forward(self, inputs):
        p3, p4, p5 = inputs
        # top-down
        p5_td = p5
        p4_td = self.w1([p4, F.interpolate(p5_td, scale_factor=2)])
        p3_td = self.w2([p3, F.interpolate(p4_td, scale_factor=2)])
        # bottom-up
        p4_out = self.w3([p4_td, F.max_pool2d(p3_td, 2)])
        p5_out = self.w4([p5_td, F.max_pool2d(p4_out, 2)])
        return self.p3_conv(p3_td), self.p4_conv(p4_out), self.p5_conv(p5_out)

